#include <stdio.h>

int main(void)
{
    char a;
    scanf("%c", &a);
    
    if ( a == 'A' ) {
        puts("1");
    }
    if ( a == 'B' ) {
        puts("2");
    }
    if ( a == 'C' ) {
        puts("3");
    }
    if ( a == 'D' ) {
        puts("4");
    }
    if ( a == 'E' ) {
        puts("5");
    }
    
    return 0;
}